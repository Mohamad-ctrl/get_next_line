#include "libft.h"

int	ft_isalnum(int n)
{
	if (n <= 90 && n >= 65)
		return (1);
	else if (n <= 122 && n >= 97)
		return (1);
	else if (n <= 57 && n >= 48)
		return (1);
	return (0);
}
